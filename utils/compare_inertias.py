import numpy as np

def frobenius_error(I_true, I_est):
    error = np.linalg.norm(I_true - I_est, 'fro')
    rel_error = 100 * error / np.linalg.norm(I_true, 'fro')
    return rel_error

def amplification_factor(I_part, I_true):
    """kappa(I_part) * relative Frobenius error"""
    kappa = np.linalg.cond(I_part)
    frob_rel = frobenius_error(I_true, I_part) / 100
    return kappa * frob_rel

def eigenvector_misalignment(I_true, I_part):
    """Average angle between principal axes (degrees)"""
    _, V_true = np.linalg.eigh(I_true)
    _, V_part = np.linalg.eigh(I_part)
    angles = []
    for i in range(3):
        for j in range(3):
            cos_theta = abs(np.dot(V_true[:,i], V_part[:,j]))
            angles.append(np.degrees(np.arccos(np.clip(cos_theta, -1, 1))))
    return np.min(angles)

def full_misalignment(I_true, I_part):
    """Average |angle| between corresponding principal axes"""
    vals_t, V_true = np.linalg.eigh(I_true)
    vals_p, V_part = np.linalg.eigh(I_part)
    
    # Sort by eigenvalue magnitude for correspondence
    idx_t = np.argsort(vals_t)[::-1] 
    idx_p = np.argsort(vals_p)[::-1]
    V_true = V_true[:, idx_t]
    V_part = V_part[:, idx_p]
    
    angles = [np.degrees(np.arccos(np.clip(abs(np.dot(V_true[:,i], V_part[:,i])), 0, 1))) 
              for i in range(3)]
    return np.mean(angles), angles  # mean, [ax1, ax2, ax3]

